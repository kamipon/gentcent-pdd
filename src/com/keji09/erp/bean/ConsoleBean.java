package com.keji09.erp.bean;

public class ConsoleBean {
	
	/**
	 * 活动数量数量
	 * */
	private Integer countActivity = 0;
	
	/**
	 * 活动模板数量
	 * */
	private Integer model = 0;

	public Integer getCountActivity() {
		return countActivity;
	}

	public void setCountActivity(Integer countActivity) {
		this.countActivity = countActivity;
	}

	public Integer getModel() {
		return model;
	}

	public void setModel(Integer model) {
		this.model = model;
	}

	
}
